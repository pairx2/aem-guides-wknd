    function drawProbePrep(index){
      var chromeProduct= "svg"+index;
      clearProbes(chromeProduct);
      var element = $('[data-attr='+chromeProduct+']');
      var probePrepCounterVar = 0;

      if(element.hasClass("accordionTitlebackground")) {
          $.each(searchResponse, function(productStatus,product){
            var doesProductHaveIdeogramDrawn = false;
            var lociStartArm, lociStartBand, lociEndArm, lociEndBand, fluoro_id, fluorophore_id, probeRange;
            $.each(product.response.vials, function(vialStatus,vial){
                if(vial.probes.length>0 && doesProductHaveIdeogramDrawn == false){
                    var vialShouldBeShownPrep = false;
                    $.each(vial.probes, function(probeStatus, probe){
                        if(probe.chromosome == chromosomeId){
                            vialShouldBeShownPrep = true;
                            doesProductHaveIdeogramDrawn = true;
                        }
                    });
                    if(vialShouldBeShownPrep == true){
                        if(probePrepCounterVar == index){
                            $.each(vial.probes, function(probeStatus, probe){
                              if(probe.chromosome == chromosomeId){
                                  if((lociStartArm == 'undefined' && lociStartBand == 'undefined' && lociEndArm == 'undefined' && lociEndBand == 'undefined') || !(lociStartArm == probe.lociStart.arm && lociStartBand == probe.lociStart.band && lociEndArm == probe.lociEnd.arm && lociEndBand == probe.lociEnd.band)) {
                                    lociStartArm = probe.lociStart.arm;
                                    lociStartBand = probe.lociStart.band;
                                    lociEndArm = probe.lociEnd.arm;
                                    lociEndBand = probe.lociEnd.band;
                                    fluorophore_id = [probe.orderInfo.flourophoreId];
                                  }
                                  else if(lociStartArm == probe.lociStart.arm && lociStartBand == probe.lociStart.band && lociEndArm == probe.lociEnd.arm && lociEndBand == probe.lociEnd.band) {
                                    fluorophore_id.push(probe.orderInfo.flourophoreId);
                                  }
                              }
                           });
                            $.each(vial.probes, function(probeStatus, probe){
                                if(probe.chromosome == chromosomeId){
                                    if(fluorophore_id.length > 1) {
                                        fluoro_id = fluorophore_id;
                                    } else {
                                        fluoro_id = [probe.orderInfo.flourophoreId];
                                    }
                                    probeRange = probe.chromosome + probe.lociStart.arm + probe.lociStart.band + "-" + probe.lociEnd.arm + probe.lociEnd.band;
                                    if (drawnGenes.indexOf(probeRange) >= -1) {
                                        drawProbes(element,
                                        [probe.lociStart.arm, probe.lociStart.band,
                                        probe.lociEnd.arm, probe.lociEnd.band,
                                        fluoro_id]);
                                        drawnGenes.push(probeRange);
                                    }
                                }
                            });
                        }
                        probePrepCounterVar = probePrepCounterVar + 1;
                    }
                }
            });
            $.each(product.response.vials, function(vialStatus,vial){
                $.each(vial.probes, function(probeStatus, probeDet){
                  var probe = probeDet.chromosome;
                  if(probeDet == chromosomeId){
                          if (probePrepCounterVar == index) {
                              drawProbes(element,
                                      [probe.lociStart.arm, probe.lociStart.band,
                                      probe.lociEnd.arm, probe.lociEnd.band, 
                                      [probe.orderInfo.flourophoreId]]);
                          }
                          probePrepCounterVar = probePrepCounterVar + 1;
                  }
                });
            });
          });
      }
      else {
          return false;
      }
  }



