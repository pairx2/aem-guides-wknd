    function drawCentromere(bottom_text, lastBandPos_text, newBandPos, color, nme, nme2) {
        var centroHeight = ((newBandPos - lastBandPos_text) * sizeMultiplier);
        var centroWidth = chromosomeWidth;
        var newBandY = bottom_text - (newBandPos * sizeMultiplier);
        var group;
        if (color == -1) {
            group = centromere;
        } else {
            group = box;
        }

        group.createRect({x:xOffset - 2, y:newBandY, width:centroWidth + 4, height:centroHeight}).setFill("white");

        var c = group.createPath().moveTo(xOffset, newBandY).hLineTo(xOffset + centroWidth);
        c.curveTo({x:xOffset + centroWidth,y:newBandY + (centroHeight * .25)}, {x:xOffset + (centroWidth * .75),y:newBandY + (centroHeight * .5)}, xOffset + (centroWidth * .66), newBandY + (centroHeight * .5));
        c.curveTo({x:xOffset + (centroWidth * .75),y:newBandY + (centroHeight * .5)}, {x:xOffset + centroWidth,y:newBandY + (centroHeight * .75)}, xOffset + centroWidth, newBandY + centroHeight);
        c.hLineTo(xOffset);
        c.curveTo({x:xOffset,y:newBandY + (centroHeight * .75)}, {x:xOffset + (centroWidth * .25),y:newBandY + (centroHeight * .5)}, xOffset + (centroWidth * .33), newBandY + (centroHeight * .5));
        c.curveTo({x:xOffset + (centroWidth * .25),y:newBandY + (centroHeight * .5)}, {x:xOffset,y:newBandY + (centroHeight * .25)}, xOffset, newBandY);
        c.setStroke("black");

        if (color == -1) {
            c.setFill(pattern);
            createGeneText(newBandY + ((centroHeight) / 2) + 4, nme);
        } else {
            c.setFill(centroPattern[color]).setStroke({color:floroFillColor[color], width:2});
            drawSelectedBox(newBandY, centroHeight, [color], "p" + nme + "-q" + nme2);
        }

        return c;
    }

