    function drawGene(bottom_text, lastBandPos_text, newBandPos, colorIndex, nme) {
        var newBandY = bottom_text - (newBandPos * sizeMultiplier);
        var newBandHeight = (newBandPos - lastBandPos_text) * sizeMultiplier;

        var fillStyle = ["#FFF","#CCC","#999", "#666", "#333", "#000"];
        var rectGene = genes.createRect({
            x : xOffset,
            y : newBandY,
            width : chromosomeWidth,
            height : newBandHeight
        });
        if (colorIndex <= 5) {
            rectGene.setStroke(fillStyle[colorIndex]).setFill(fillStyle[colorIndex]);
        } else if (colorIndex == 7.3) {
            rectGene.setFill(pattern);
        } else if (colorIndex == 10.3) {
            rectGene.setFill(pattern2);
        }

        if (newBandHeight > 7.5) {      //only prints the label if the band is taller than the text
            createGeneText(newBandY + ((newBandHeight) / 2) + 4, nme);
        }
        return rectGene;
    }


