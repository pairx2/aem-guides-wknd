    function drawProbes(element, arr){
        for(var i=0;i<arr.length;i+=5){
            var arm = arr[i];
            var nme = arr[i+1];
            var endArm = arr[i+2];
            var endNme = arr[i+3];
            var colors = arr[i+4];
            var startingBottomY,startingTopY, endBottomY, endTopY;
            if(((nme == ' Telomere')||(nme == 'TELOMERE')||(nme =='p Telomere'))&&(arm=='P')){
                drawSelectedBox(40, (2)*sizeMultiplier,  colors, 'p Telomere');
            }
            else if(((nme == ' Telomere')||(nme == 'TELOMERE')||(nme =='q Telomere'))&&(arm=='Q')){
                drawSelectedBox(bottom-(2*sizeMultiplier), (2)*sizeMultiplier,  colors, 'q Telomere');
            }
            else {
				getInfo(arm, nme);

                startingBottomY = lastProbeY;
                startingTopY = newProbeY;
                arm = arm.toLowerCase();
                var displayName = arm+nme;
                if(getInfo(endArm,endNme)){
                    endBottomY = lastProbeY;
                    endTopY = newProbeY;
                    if(arm!=endArm.toLowerCase()){
                        if(startingBottomY<endBottomY){
                            drawCentromere(bottom, startingBottomY, endTopY, colors[0], nme, endNme);
                        }else{
                            drawCentromere(bottom, endBottomY, startingTopY, colors[0], nme, endNme);
                        }
                    }else{
                        if(nme!=endNme){ displayName+='-'+endNme; }
                        if(startingBottomY<endBottomY){
                            drawSelectedBox(bottom-(endTopY*sizeMultiplier), (endTopY-startingBottomY)*sizeMultiplier,  colors, displayName);
                        }else{
                            drawSelectedBox(bottom-(startingTopY*sizeMultiplier), (startingTopY-endBottomY)*sizeMultiplier,  colors, displayName);
                        }
                    }
                }
                else{
                  if(startingTopY != 0 && startingBottomY !=0) {
                    drawSelectedBox(bottom-(startingTopY*sizeMultiplier), (startingTopY-startingBottomY)*sizeMultiplier,  colors, displayName);
                  }
                }
            }
        }
    }

    //==================================================================================================================
    // GET INFO
    //==================================================================================================================
    function getInfo(arm, nme){
        lastBandName = 0;
        lastProbeY = 0;
        newProbeY = 0;
        var retValue = 0;

        $.each(chromosomes, function(ChromosomeStatus,chromosomeVar){
          if(ChromosomeStatus =='22'){
            ChromosomeStatus = 'X';
          }
           else if(ChromosomeStatus == '23'){
            ChromosomeStatus = 'Y';
           }
           else{
              ChromosomeStatus = ChromosomeStatus+1;
           }
            if(ChromosomeStatus == chromosomeId){
                if((ChromosomeStatus<= 19 && ChromosomeStatus>=15) || (ChromosomeStatus<= 11) || (ChromosomeStatus>21) || (ChromosomeStatus=='X') || (ChromosomeStatus=='Y')){
                    if(arm=='Q'){
                        $.each(chromosomeVar.q.band, function(bandStatus,band){
                            if(bandStatus != 0){
                                if(lastBandName == nme){
                                    newProbeY = band.position;
                                    lastProbeY = lastBandPos;
                                    retValue = 1;
                                    return false;
                                }
                                if(bandStatus == chromosomeVar.q.band.length - 1){
                                      if(band.bandName == nme){
                                        newProbeY = band.position+2.5;
                                        lastProbeY = band.position;
                                        retValue = 1;
                                        return false;
                                    }
                                }
                            }
                        updateBand(band.position, band.color, band.bandName);
                        });
                        
                    }else{
                        $.each(chromosomeVar.p.band, function(bandStatus,band){
                            if(bandStatus != 0){
                                if(lastBandName == nme){
                                    newProbeY = band.position;
                                    lastProbeY = lastBandPos;
                                    retValue = 1;
                                    return false;
                                }
                            }
                            if (band.bandName != "") {
                                updateBand(band.position, band.color, band.bandName);
                            }
                        });
                        
                    }
                    return false;
                }
                else {
                    if(arm=='Q'){
                        $.each(chromosomeVar.q.band, function(bandStatus,band){
                        if(bandStatus != 0){
                            if(lastBandName == nme){
                                    newProbeY = band.position;
                                    lastProbeY = lastBandPos;                                    
                                    retValue = 1;
                                    return false;
                                }
                                if(bandStatus == chromosomeVar.q.band.length-1){
                                    if(band.bandName == nme){
                                        newProbeY = band.position+2.5;
                                        lastProbeY = band.position;
                                        retValue = 1;
                                        return false;
                                    }
                                }
                            }
                            updateBand(band.position, band.color, band.bandName);
                        });
                    }else{
                        
                        $.each(chromosomeVar.p.band, function(bandStatus,band){
                            if(bandStatus != 0){
                                if(lastBandName == nme){
                                    newProbeY = band.position;
                                    lastProbeY = lastBandPos;
                                    return false;
                                }
                            }
                            updateBand(band.position, band.color, band.bandName);
                        });
                    }
                    return false;
                }
            }

        });
        return retValue;
    }

