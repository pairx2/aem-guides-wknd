public class SliderImagesBean {

    /** The media image. */
    private String mediaImage;

    /** The thumb image. */
    private String thumbImage;

    /**
     * Gets the thumb image.
     *
     * @return the thumb image
     */
    public String getThumbImage() {
        return thumbImage;
    }

    /**
     * Sets the thumb image.
     *
     * @param thumbImage the new thumb image
     */
    public void setThumbImage(String thumbImage) {
        this.thumbImage = thumbImage;
    }

    /**
     * Gets the media image.
     *
     * @return the media image
     */
    public String getMediaImage() {
        return mediaImage;
    }

    /**
     * Sets the media image.
     *
     * @param mediaImage the new media image
     */
    public void setMediaImage(String mediaImage) {
        this.mediaImage = mediaImage;
    }
}
